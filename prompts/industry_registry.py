def get_industry_prompt(industry_name: str) -> str:
    """根据行业名称返回 System Prompt"""
    dimensions = INDUSTRY_DIMENSIONS.get(industry_name, INDUSTRY_DIMENSIONS["default"])
    return CORE_RULES + "\n" + dimensions + "\n\n" + OUTPUT_FORMAT


def get_industries() -> list[str]:
    """返回所有支持的行业列表"""
    return ["default"] + [k for k in INDUSTRY_DIMENSIONS if k != "default"]
